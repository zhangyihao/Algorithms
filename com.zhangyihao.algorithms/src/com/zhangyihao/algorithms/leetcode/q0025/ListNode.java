package com.zhangyihao.algorithms.leetcode.q0025;

/**
 * Definition for singly-linked list
 * @author zhangyihao
 */
public class ListNode {
    public int val;
    public ListNode next;

    ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
